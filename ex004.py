inf = (input('Digite algo e descubra informações detalhadas: '))
print('O tipo primitivo desse valor é', type(inf))
print('A informação é um algarismo numérico?', inf.isnumeric())
print('A informação está em caixa alta?', inf.isupper())
print('A informação é uma letra?', inf.isalpha())
print(f'A informação {inf} só tem espaço?', inf.isspace())
